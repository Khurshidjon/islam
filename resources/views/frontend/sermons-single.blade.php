                        <h2>YUNUS</h2>
                        <div class="blog-post-meta">
                            <p><i class="icofont icofont-calendar"></i> October 29, 2017</p>
                            <p><i class="icofont icofont-comment"></i> 3 Comment</p>
                            <p><i class="icofont icofont-user"></i> Posted By: <a href="#">Admin</a></p>
                        </div>
                        <p>1. Alif-Lam-Ra. [These letters are one of the miracles of the Qur’an, and none but Allah (Alone) knows their meanings]. These are the Verses of the Book (the Qur’an) Al-Hakim [showing lawful and unlawful things, explaining Allah’s (Divine) Laws for mankind, leading them to eternal happiness by ordering them to follow the true Islamic Monotheism, – worshipping none but Allah Alone – that will guide them to Paradise and save them from Hell].</p>
                        <p>2. Is it wonder for mankind that We have sent Our Inspiration to a man from among themselves (i.e. Prophet Muhammad ) (saying): “Warn mankind (of the coming torment in Hell), and give good news to those who believe (in the Oneness of Allah and in His Prophet Muhammad ) that they shall have with their Lord the rewards of their good deeds?” (But) the disbelievers say: “This is indeed an evident sorcerer (i.e. Prophet Muhammad and the Qur’an)!</p>
                        <p>3. Surely, your Lord is Allah Who created the heavens and the earth in six Days and then Istawa (rose over) the Throne (really in a manner that suits His Majesty), disposing the affair of all things. No intercessor (can plead with Him) except after His Leave. That is Allah, your Lord; so worship Him (Alone). Then, will you not remember?</p>
                        <p>4. To Him is the return of all of you. The Promise of Allah is true. It is He Who begins the creation and then will repeat it, that He may reward with justice those who believed (in the Oneness of Allah – Islamic Monotheism) and did deeds of righteousness. But those who disbelieved will have a drink of boiling fluids and painful torment because they used to disbelieve.</p>
                        <p>5. It is He Who made the sun a shining thing and the moon as a light and measured out its (their) stages, that you might know the number of years and the reckoning. Allah did not create this but in truth. He explains the Ayat (proofs, evidences, verses, lessons, signs, revelations, etc.) in detail for people who have knowledge.</p>
                        <p>6. Verily, in the alternation of the night and the day and in all that Allah has created in the heavens and the earth are Ayat (proofs, evidences, verses, lessons, signs, revelations, etc.) for those people who keep their duty to Allah, and fear Him much.</p>
                        <p>7. Verily, those who hope not for their meeting with Us, but are pleased and satisfied with the life of the present world, and those who are heedless of Our Ayat (proofs, evidences, verses, lessons, signs, revelations, etc.),</p>
                        <p>8. Those, their abode will be the Fire, because of what they used to earn.</p>
                        <p>9. Verily, those who believe [in the Oneness of Allah along with the six articles of Faith, i.e. to believe in Allah, His Angels, His Books, His Messengers, Day of Resurrection, and Al-Qadar (Divine Preordainments) – Islamic Monotheism], and do deeds of righteousness, their Lord will guide them through their Faith; under them will flow rivers in the Gardens of delight (Paradise).</p>
                        <p>10. Their way of request therein will be Subhanaka Allahumma (Glory to You, O Allah!) and Salam (peace, safe from each and every evil) will be their greetings therein (Paradise)! and the close of their request will be: Al-Hamdu Lillahi Rabbil-‘Alamin [All the praises and thanks are to Allah, the Lord of ‘Alamin (mankind, jinns and all that exists)]</p>
                        <p>11. And were Allah to hasten for mankind the evil (they invoke for themselves and for their children, etc. while in a state of anger) as He hastens for them the good (they invoke) then they would have been ruined. So We leave those who expect not their meeting with Us, in their trespasses, wandering blindly in distraction. (Tafsir At-Tabari; Vol. 11, Page 91)</p>
                        <p>12. And when harm touches man, he invokes Us, lying down on his side, or sitting or standing. But when We have removed his harm from him, he passes on his way as if he had never invoked Us for a harm that touched him! Thus it seems fair to the Musrifun that which they used to do.</p>
                        <p>13. And indeed, We destroyed generations before you, when they did wrong while their Messengers came to them with clear proofs, but they were not such as to believe! Thus do We requite the people who are Mujrimun (disbelievers, polytheists, sinners, criminals, etc.).</p>
                        <p>14. Then We made you follow after them, generations after generations in the land, that We might see how you would work!</p>
                        <p>15. And when Our Clear Verses are recited unto them, those who hope not for their meeting with Us, say: Bring us a Qur’an other than this, or change it.”Say (O Muhammad ): “It is not for me to change it on my own accord; I only follow that which is revealed unto me. Verily, I fear if I were to disobey my Lord, the torment of the Great Day (i.e. the Day of Resurrection).”</p>
                        <p>16. Say (O Muhammad ): “If Allah had so willed, I should not have recited it to you nor would He have made it known to you. Verily, I have stayed amongst you a life time before this. Have you then no sense?”</p>
                        <p>17. So who does more wrong than he who forges a lie against Allah or denies His Ayat (proofs, evidences, verses, lessons, signs, revelations, etc.)? Surely, the Mujrimun (criminals, sinners, disbelievers and polytheists) will never be successful!</p>
                        <p>18. And they worship besides Allah things that hurt them not, nor profit them, and they say: “These are our intercessors with Allah.” Say: “Do you inform Allah of that which He knows not in the heavens and on the earth?” Glorified and Exalted be He above all that which they associate as partners with Him!</p>
                        <p>19. Mankind were but one community (i.e. on one religion – Islamic Monotheism), then they differed (later), and had not it been for a Word that went forth before from your Lord, it would have been settled between them regarding what they differed </p>
                        <p>20. And they say: “How is it that not a sign is sent down on him from his Lord?” Say: “The unseen belongs to Allah Alone, so wait you, verily I am with you among those who wait (for Allah’s Judgement).”</p>
